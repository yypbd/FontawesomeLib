    public String getText(FontType fontType, String iconName) {
        switch (fontType) {
            case TYPE_SOLID:
                if (mMapSolid.containsKey(iconName)) {
                    return String.valueOf(mMapSolid.get(iconName));
                }
            case TYPE_REGULAR:
                if (mMapRegular.containsKey(iconName)) {
                    return String.valueOf(mMapRegular.get(iconName));
                }
            case TYPE_BRAND:
                if (mMapBrand.containsKey(iconName)) {
                    return String.valueOf(mMapBrand.get(iconName));
                }
        }

        return null;
    }

    public void setIcon(FontType fontType, String iconName, TextView textView) {
        String text = getText(fontType, iconName);
        switch (fontType) {
            case TYPE_SOLID:
                if (text != null) {
                    textView.setTypeface(mTypefaceSolid);
                }
                break;
            case TYPE_REGULAR:
                if (text != null) {
                    textView.setTypeface(mTypefaceRegular);
                }
                break;
            case TYPE_BRAND:
                if (text != null) {
                    textView.setTypeface(mTypefaceBrand);
                }
                break;
        }
        textView.setText(text);
    }

